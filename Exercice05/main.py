def add(a, b):
    """Additionne deux nombres.

    Args:
        a (number): Premier nombre.
        b (number): Deuxième nombre.

    Returns:
        nombre : La somme de a et b.
    """
    return a + b


def subtraction(a, b):
    """Soustrait le premier nombre avec le deuxieme.

    Args:
        a (number): Premier nombre.
        b (number): Deuxième nombre.

    Returns:
        nombre : La différence entre a et b.
    """
    return a - b
